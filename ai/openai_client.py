from openai import AsyncOpenAI

from config.config import OPENROUTER_API_KEY

client = AsyncOpenAI(
    api_key=OPENROUTER_API_KEY,
    base_url="https://openrouter.ai/api/v1"
)


async def generate_final_report(
    questions,
    answers
):
    """
    Формирует итоговый психологический отчёт.

    Объединяет вопросы и ответы пользователя,
    отправляет их в модель DeepSeek через OpenRouter
    и возвращает структурированный ознакомительный отчёт.

    Args:
        questions (list): Список вопросов интервью.
        answers (list): Ответы пользователя.

    Returns:
        str: Итоговый отчёт, сформированный искусственным интеллектом.
    """

    interview = ""

    # Формирование текста интервью
    for q, a in zip(
        questions,
        answers
    ):
        interview += (
            f"Вопрос: {q}\n"
            f"Ответ: {a}\n\n"
        )

    # Отправка интервью в модель искусственного интеллекта
    response = await client.chat.completions.create(
        model="deepseek/deepseek-chat-v3-0324",
        temperature=0.4,
        messages=[
            {
                "role": "system",
                "content": """
Ты анализируешь интервью пользователя.

Очень важные правила:

1. Используй ТОЛЬКО факты из ответов пользователя.
2. Никогда не придумывай причины, мотивы или скрытые факторы.
3. Никогда не пиши:
   - возможно
   - вероятно
   - может быть связано
   - внутренние конфликты
   - скрытые причины
   если пользователь об этом не говорил.

4. Если информации недостаточно, пиши:
   "Недостаточно информации."

5. Не ставь диагнозы.
6. Не определяй расстройства.
7. Не делай медицинские выводы.

Структура отчёта:

📊 Что сообщил пользователь

😊 Эмоциональное состояние

😴 Сон

⚡ Энергия

🔥 Стресс

👥 Социальная поддержка

💪 Ресурсы и сильные стороны

📝 Практические рекомендации

Отдельно укажи:

❓ Что осталось неясным

В конце обязательно напиши:

Данный отчёт носит ознакомительный характер и не является медицинским диагнозом.
"""
            },
            {
                "role": "user",
                "content": interview
            }
        ]
    )

    # Возвращаем сформированный отчёт
    return response.choices[0].message.content
